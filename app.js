const container = document.getElementsByClassName("row")[1];

for (let i of cars.list) {
    const showtext = cars.list.map((i) => {
        return `
            <div class="card mx-4 my-4 ${i.category}" style="width: 18rem;">
            <img src="${i.image}" class="card-img-top" alt="...">
            <div class="card-body">
                <h5 class="card-title headingColor carName">${i.name.toUpperCase()}</h5>
                <p class="card-text priceColor">${i.price}</p>
                <img src="${i.logo}" width="60" height="70">
            </div>
            </div>
            `
    })
    let comma = showtext.join("")
    container.innerHTML = comma
}

function buttons(value) {
    let elements = document.querySelectorAll(".card")

    elements.forEach((element) => {
        if (value == "all") {
            element.classList.remove("hide")
        } else {
            if (element.classList.contains(value)) {
                element.classList.remove("hide")
            } else {
                element.classList.add("hide")
            }
        }
    })

}

document.getElementById("input-btn").addEventListener("click", () => {
    let searchInput = document.getElementById("search").value;
    let elements = document.querySelectorAll(".carName");
    let cards = document.querySelectorAll(".card");

    elements.forEach((element, index) => {
        if (element.innerText.includes(searchInput.toUpperCase())) {
            cards[index].classList.remove("hide");
        } else {
            cards[index].classList.add("hide");
        }
    });
});

window.onload = ()=>{
    buttons('all')
}